export function getStretchesForBodyPart(bodyPart: string): Stretch[] {
  return stretches.filter((s) => s.bodyPart === bodyPart);
}

export function pickRandomStretches(bodyPart: string, count: number): Stretch[] {
  const pool = getStretchesForBodyPart(bodyPart);
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, pool.length));
}
